# Manipulação de strings
# Crie uma variável chamada “nome” e atribua a ela o valor do seu nome.
# Imprima o comprimento do nome utilizando a função “len”.
# Converta o nome para letras maiúsculas utilizando o método “upper”.
# Imprima o nome em letras maiúsculas.

# Atribuindo o nome à variável
nome = "Flaviano"

# Imprimindo o comprimento do nome
print(f'Comprimento do nome: {len(nome)}')

# Convertendo para letras maiúsculas
nome_maiusculo = nome.upper()

# Imprimindo o nome em letras maiúsculas
print(f'Nome em letras maiúsculas: {nome_maiusculo}')
